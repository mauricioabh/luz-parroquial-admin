export function Sidebar() {
  const pathname = usePathname()
  const { profile } = useProfile()

  if (!profile) {
    return null
  }

  // Show sidebar for admin roles or diocese admins
  const isAdmin = ADMIN_ROLES.includes(profile.role_name as any)
  const isDioceseAdmin = profile.role_name === 'diocese_admin'

  if (!isAdmin && !isDioceseAdmin) {
    return null
  }

  // Select appropriate nav items based on role
  const navItems = isDioceseAdmin ? dioceseNavItems : adminNavItems

  return (
    <aside className="hidden lg:block fixed left-0 top-16 h-[calc(100vh-4rem)] w-64 bg-card border-r border-border overflow-y-auto">
      <nav className="p-4 space-y-1">
        {navItems.map((item) => {
          const isActive = pathname === item.href || (item.href !== '/' && pathname?.startsWith(item.href))
          const Icon = item.icon
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-4 py-3 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-primary text-primary-foreground'
                  : 'text-muted-foreground hover:bg-secondary hover:text-foreground'
              }`}
            >
              <Icon />
              {item.label}
            </Link>
          )
        })}
      </nav>
    </aside>
  )
}

